<h4 id="Einwilligungserklaerung">Einwilligungserklärung für personenbezogene Zusatzdaten:</h4>

<p>
    Ihre personenbezogenen Zusatzdaten werden gemäß DSGVO Art. 6 Abs. 1 lit. a von MinD-Hochschul-Netzwerk (MHN) im
    Referenten-Tool verarbeitet und gespeichert. Diese Daten sind im Formular als optional gekennzeichnet.
    Diese Daten dienen dem Zwecke der vereinfachterenOrganisation der Veranstaltung. Diese Daten werden ausschließlich
    für die Organisation genutzt, nicht ohne Ihre Zustimmung weitergegeben und zum Ende der Veranstaltung gelöscht.
    Einsehen können die Daten Personen, die Teil des Organisationsteams sind. Die Eingabe der Zusatzdaten ist freiwillig
    und es bestehen keine Folgen sollten diese nicht angegeben werden.
</p>

<p>
    Die MHN-Server werden von einem Drittanbieter gemäß einer Auftragsverarbeitung gehostet und vom MHN-IT-Team
    ehrenamtlich betrieben und gewartet. Wenn du die Daten selbst im Formular löscht, werden sie unverzüglich auf dem
    Server gelöscht; wennSie die Löschung Ihrer Daten beim zuständigen Vorstandsmitglied unter
    vorstand@mind-hochschul-netzwerk.de beantragen, werden sie innerhalb von zwei Wochen gelöscht.
    Für die Verarbeitung und Speicherung der Daten verantwortlich ist das für die Organisation der Veranstaltung
    zuständige MHN-Vorstandsmitglied, erreichbar unter vorstand@mind-hochschul-netzwerk.de. Sie haben das Recht,
    Auskunft über die über Sie gespeicherten Daten zu erhalten, sie zu berichtigen und zu löschen, ihre Verarbeitung
    einzuschränken oder ihr zu widersprechen sowie Beschwerde bei einer Aufsichtsbehörde einzulegen. Zudem haben Sie
    jederzeit das Recht, dieser Einwilligung zu widersprechen. Bitte wenden Sie sich dazu an das zuständige
    Vorstandsmitglied unter vorstand@mind-hochschul-netzwerk.de. Weiterführende Informationen zur Datenverarbeitung bei
    MHN finden Sie in der <a href="https://www.mind-hochschul-netzwerk.de/index.php/datenschutz/">Datenverarbeitungsrichtlinie</a>.
</p>



