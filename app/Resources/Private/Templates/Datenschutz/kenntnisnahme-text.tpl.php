<h4 id="Informationspflicht">Informationspflicht bei Erhebung von personenbezogenen Daten Art. 13 DSGVO:</h4>

<p>
    Ihre personenbezogenen Daten werden verarbeitet und gespeichert. Dies umfasst nach DSGVO Art. 6 Abs. 1 lit. b Daten
    die zur Durchführung der Veranstaltung notwendig sind. Teile der Daten (Name, Vortragstitel, Abstract, Kurzvita)
    werden dauerhaft auf der Veranstaltungshomepage öffentlich zugänglich gemacht; alle anderen Daten werden
    unverzüglich nach der Veranstaltung gelöscht. Personenbezogene Daten, die für die Abrechnung genutzt werden,
    unterliegen einer steuerrechlichen Speicherungspflicht und werden nach Ablauf entsprechender Fristen gelöscht (DSGVO
    Art. 6 Abs. 1 lit. c).
    Die Daten werden ausschließlich für die genannten Punkte genutzt und nicht ohne Ihre Zustimmung weitergegeben. Wenn
    nicht weiteres Einverständnis gegeben wurde, dann können diese Daten nur von Personen eingesehen werden, die diese
    zur Durchführung der Veranstaltung benötigen.
</p>

<p>
    Sie haben das Recht, Auskunft über die über Sie gespeicherten Daten zu erhalten, sie zu berichtigen und zu löschen,
    ihre Verarbeitung einzuschränken oder ihr zu widersprechen sowie Beschwerde bei einer Aufsichtsbehörde einzulegen.
    Zudem haben Sie jederzeit das Recht, dieser Einwilligung zu widersprechen. Bitte wenden Sie sich dazu an das
    zuständige Vorstandsmitglied unter vorstand@mind-hochschul-netzwerk.de. Weiterführende Informationen zur
    Datenverarbeitung bei MHN finden Sie in der <a href="https://www.<?=getenv('DOMAINNAME')?>/mod/page/view.php?id=12">Datenverarbeitungsrichtlinie</a>.
</p>




